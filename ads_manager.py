# ── Insights / Reporting ───────────────────────────────────────────────────────

def get_campaign_insights(campaign_id, date_preset="last_7d"):
    campaign = Campaign(campaign_id)
    insights = campaign.get_insights(fields=[
        "impressions", "clicks", "spend", "ctr", "cpc", "reach", "frequency",
    ], params={"date_preset": date_preset})
    for row in insights:
        print(
            f"Impressions: {row.get('impressions')}  "
            f"Clicks: {row.get('clicks')}  "
            f"Spend: Rs.{row.get('spend')}  "
            f"CTR: {row.get('ctr')}%"
        )
    return insights


def get_account_insights(date_preset="last_7d"):
    account = init()
    insights = account.get_insights(fields=[
        "impressions", "clicks", "spend", "ctr", "cpc", "reach", "campaign_name",
    ], params={
        "date_preset": date_preset,
        "level": "campaign",
    })
    for row in insights:
        print(
            f"{row.get('campaign_name'):40s}  "
            f"Spend: Rs.{row.get('spend'):>8}  "
            f"Impressions: {row.get('impressions'):>10}  "
            f"CTR: {row.get('ctr')}%"
        )
    return insights
