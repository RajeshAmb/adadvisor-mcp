# ── Campaigns ──────────────────────────────────────────────────────────────────

def create_campaign(name, objective="OUTCOME_TRAFFIC", status="PAUSED"):
    account = init()
    campaign = account.create_campaign(fields=[], params={
        Campaign.Field.name: name,
        Campaign.Field.objective: objective,
        Campaign.Field.status: status,
        Campaign.Field.special_ad_categories: [],
        "is_adset_budget_sharing_enabled": False,
    })
    print(f"Created campaign: {campaign['id']} — {name}")
    return campaign


def list_campaigns():
    account = init()
    campaigns = account.get_campaigns(fields=[
        Campaign.Field.id,
        Campaign.Field.name,
        Campaign.Field.status,
        Campaign.Field.objective,
    ])
    for c in campaigns:
        print(f"[{c['status']}] {c['id']} — {c['name']} ({c['objective']})")
    return campaigns


def update_campaign_status(campaign_id, status):
    campaign = Campaign(campaign_id)
    campaign.api_update(params={Campaign.Field.status: status})
    print(f"Campaign {campaign_id} set to {status}")


# ── Ad Sets ────────────────────────────────────────────────────────────────────

def create_adset(campaign_id, name, daily_budget_cents, billing_event="IMPRESSIONS",
                 optimization_goal="REACH", targeting=None, status="PAUSED",
                 bid_strategy="LOWEST_COST_WITHOUT_CAP"):
    account = init()
    if targeting is None:
        targeting = {
            "geo_locations": {"countries": ["IN"]},
            "age_min": 18,
        }
    adset = account.create_ad_set(fields=[], params={
        AdSet.Field.name: name,
        AdSet.Field.campaign_id: campaign_id,
        AdSet.Field.daily_budget: daily_budget_cents,
        AdSet.Field.billing_event: billing_event,
        AdSet.Field.optimization_goal: optimization_goal,
        AdSet.Field.targeting: targeting,
        AdSet.Field.status: status,
        AdSet.Field.bid_strategy: bid_strategy,
        AdSet.Field.promoted_object: {"page_id": "1053689994488653"},
    })
    print(f"Created ad set: {adset['id']} — {name}")
    return adset


def list_adsets(campaign_id):
    campaign = Campaign(campaign_id)
    adsets = campaign.get_ad_sets(fields=[
        AdSet.Field.id,
        AdSet.Field.name,
        AdSet.Field.status,
        AdSet.Field.daily_budget,
    ])
    for a in adsets:
        budget = int(a.get("daily_budget", 0)) / 100
        print(f"[{a['status']}] {a['id']} — {a['name']} (Rs.{budget:.2f}/day)")
    return adsets


def update_adset_budget(adset_id, daily_budget_cents):
    adset = AdSet(adset_id)
    adset.api_update(params={AdSet.Field.daily_budget: daily_budget_cents})
    print(f"Ad set {adset_id} budget updated to Rs.{daily_budget_cents/100:.2f}/day")


