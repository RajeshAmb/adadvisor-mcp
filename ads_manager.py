def upload_image(image_path):
    account = init()
    image = account.create_ad_image(params={
        AdImage.Field.filename: image_path,
    })
    image.api_get(fields=[AdImage.Field.hash])
    print(f"Uploaded image hash: {image['hash']}")
    return image["hash"]


